#ifndef MAIN_H
#define MAIN_H

#include <stdio.h>
/**
 * _isupper - checks for uppercase character
 * @c: is the character to check
 * Return: 1 if c is uppercase, else 0
 */
int _isupper(int c);

/**
 * _isdigit - checks for a digit 0-9
 * @c: is the character to check
 * Return: 1 if c is a digit, else 0
 */

int _isdigit(int c);

#endif /* MAIN_H */
